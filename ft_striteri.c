#include "libft.h"

void				ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	i = 0;
	if (s != NULL && f != NULL)
	{
		while (s[i] != '\0' && s != NULL)
		{
			f(i, &s[i]);
			i++;
		}
	}
}
