#include "libft.h"

int		ft_str_is_lowercase(char *s)
{
	if (!s && ft_str_is_alpha(s) == 0)
		return (0);
	while (*s)
	{
		if (!(*s >= 'a' && *s <= 'z'))
			return (0);
		s++;
	}
	return (1);
}
